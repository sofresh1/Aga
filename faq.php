<h5>Eau :</h5> Pour lire le compteur d’eau, il vous suffit de relever l’index (les chiffres) qui se trouve dans la partie gauche de la lucarne, ils correspondent au mètres cube (m3). Les chiffres en noirs, quatre en principe (roues), constituent ce qu’on appelle l’index, que vous trouvez sur chaque facture avec relevé.
Sur le cadran figure aussi une suite de chiffres inscrits en rouge ; centaine de litres, dizaine de litres et litres.

<h5>Attention !</h5>Les chiffres en rouge (ou en blanc sur fond rouge) indiquent les litres consommés. Ils ne sont pas pris en compte dans la facturation exprimée en m3, mais peuvent vous servir à détecter une fuite ou à vérifier la consommation d’un appareil ménager.

<h5>Rappel :</h5>Un mètre cube (m3) égal 1 000 litres soit une tonne d’eau !
<h2>4 - Comment evaluer ca consommation</h2>
<p>
