Notez qu'il est possible de réduire sa consommation de manière non négligeable, en investissant quelques poignées d'euros dans des accessoires tels qu'un pommeau à économie d'eau pour la douche, des régulateurs de jet pour l'évier et le lavabo, des écoplaquettes pour les WC...
On estime qu’un ménage consacre environ 60 % de son eau – potable – à l’hygiène corporelle et domestique et à l’évacuation des toilettes, et seulement 1 % à la boisson ! Le reste passe dans la préparation de la nourriture (6 %), le lavage de la vaisselle (10 %), du linge (12 %)…
Pour donner des ordres de grandeur, la consommation d’une personne avoisine les 50 à 60 m3 par an (150 à 165 litres par jour), soit une facture oscillant entre 150 et 240 € selon la situation géographique, le gestionnaire (régie publique ou entreprise privée) et le montant des taxes locales.
<h2>5-Les bons réflexes</h2>
<p>
• N’attendez jamais pour changer le joint d’un robinet qui goutte ou faire réparer un WC qui fuit. Les fuites d’une chasse d’eau peuvent dépasser les 20 litres par heure, soit 175 m3 par an – la consommation de notre famille type en serait doublée !</br>
<p>
• Une façon simple d’économiser de l’eau est de ne pas ouvrir le robinet à tort et à travers. Par exemple, fermez-le quand vous vous brossez les dents. Laisser ouvert un robinet dont le débit moyen est de 12 l/min, c’est gaspiller des milliers de litres chaque année.
</p>
<p>
• Vous changez de robinetterie ? Adoptez le mitigeur : vous réglez le débit et la température en actionnant une seule commande. Le robinet thermostatique est encore mieux adapté aux douches : une manette règle le débit, une autre permet de fixer la température souhaitée.
</p>
<p>
• Quand votre lave-vaisselle ou votre lave-linge vous lâche, regardez bien l’étiquette énergie de son remplaçant pressenti : elle mentionne non seulement la consommation d’électricité, mais aussi la consommation d’eau. Notez qu’une nouvelle étiquette énergie apparaît petit à petit dans les magasins ; elle sera obligatoire à la fin de l’année 2011. Pour en savoir plus, consultez la rubrique « Bien acheter » du site de l’Ademe.
</p>
<p>
• Enfin, si vous disposez d’un compteur individuel d’eau, vérifiez-le régulièrement pour constater d’éventuelles anomalies. De petites fuites – notamment dans les canalisations enterrées dans les jardins – ont très vite de gros impacts sur la facture. Et si vous n’avez pas de compteur individuel, sollicitez votre syndicat de copropriété ou votre bailleur pour en faire installer un. En effet, un compteur collectif est peu incitatif : vous ne constatez pas clairement (voire pas du tout) l’impact de vos efforts sur le volume consommé. Et le gaspillage des uns pèse directement sur les factures des autres…
</p>
